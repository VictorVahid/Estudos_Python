#Tuples (S√ÉO LISTAS IMUTAVEIS)


#Utilizando sem colchetes
nomes = 'Maria', 'Vahid', 'Theia'

print(nomes[-1])
print(nomes)

#Utilizando com parentese
nomes1 = ('Maria', 'Vahid', 'Theia')
print(nomes1)

#Conversao de lista para tuple
nomes2 = ['Maria', 'Vahid', 'Theia']
conversao_tuple = tuple(nomes2)
print(conversao_tuple)